# Operadores aritméticos
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(3 % 4)
print(3 // 4)  # división aproximada a un número entero
print(3 ** 4)  # capturar un exponente, da el resultado de 3 elevado a 4

print("hola" + "python")
print("hola " + str(5))
print("hola " * 5)

# Operadores comparativos
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)
print("aaa" > "nnn")  # Al utilizar signos de comparación <,>,<=,>= hace una ordenación alfabética

# Operadores lógicos
print(3 < 4 and "aaa" > "nnn")
print(3 < 4 or "aaa" > "nnn")
print(not(3 < 4 or "aaa" > "nnn"))